#include"main.h"

/**
 * print_sign - function to check positive and negative
 * @n: input of function
 * Return: 1 if alphapetic or 0 if not
 */
int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	else if (n == 0)
	{
		_putchar('0');
		return (0);
	}
	else if (n < 0)
	{
		_putchar('-');
		return (-1);
	}
	else
	{
		_putchar('-');
		return ('/');
	}
}

